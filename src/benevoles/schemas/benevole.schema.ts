import * as mongoose from 'mongoose';

export const BenevoleSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
  },
  firstname: {
    type: String,
    required: true,
    trim: true,
  },
  lastname: {
    type: String,
    required: true,
    trim: true,
  }
}, {
  toJSON: { virtuals: true },
  versionKey: false,
});
